def write_version_file(version):
    """
    将版本号写入 src/_version.py 文件。
    """
    version_file = Path("src") / "_version.py"
    print(f"正在写入版本号到: {version_file}")
    try:
        with open(version_file, "w", encoding='utf-8') as f:
            f.write(f'# This file is auto-generated by build.py\n')
            f.write(f'__version__ = "{version}"\n')
        print("版本文件写入成功。")
        return True
    except IOError as e:
        print(f"错误: 写入文件 {version_file} 失败: {e}")
        return False

def run_pyinstaller():
    """
    执行 PyInstaller 打包命令。
    """
    spec_file = "autofish_release.spec"
    print(f"开始执行 PyInstaller，使用 spec 文件: {spec_file}...")
    try:
        subprocess.run(
            [sys.executable, "-m", "PyInstaller", spec_file],
            check=True,
            encoding='utf-8'
        )
        print("PyInstaller 打包成功完成。")
        return True
    except FileNotFoundError:
        print(f"错误: 找不到 spec 文件 '{spec_file}'。")
        return False
    except subprocess.CalledProcessError:
        print("错误: PyInstaller 打包过程中发生错误。")
        return False

def post_build_actions(version):
    """
    打包后的后续操作：复制资源并重命名输出文件夹。
    """
    dist_path = Path("dist") / "autofish"
    target_path = Path("dist") / f"autofish-{version}"
    resources_src = Path("resources")
    resources_dst = dist_path / "resources"

    print(f"--- 开始打包后处理 (版本: {version}) ---")

    # 1. 复制 resources 目录
    if resources_src.exists():
        print(f"正在复制 resources 到 {resources_dst}...")
        try:
            if resources_dst.exists():
                shutil.rmtree(resources_dst)
            shutil.copytree(resources_src, resources_dst)
            print("resources 目录复制成功。")
        except Exception as e:
            print(f"警告: 复制 resources 目录失败: {e}")
    else:
        print("警告: 未找到 resources 目录，跳过复制。")

    # 1.5 Config directory is no longer needed in dist (generated at runtime in user_data_dir)
    # fish.json is now in resources
    print("跳过复制 config 目录 (已改为用户数据目录存储)")

    # 1.6 复制 使用说明与版本更新.txt
    readme_src = Path("使用说明与版本更新.txt")
    readme_dst = dist_path / "使用说明与版本更新.txt"
    if readme_src.exists():
        print(f"正在复制 {readme_src} 到 {readme_dst}...")
        try:
            shutil.copy2(readme_src, readme_dst)
            print("使用说明与版本更新.txt 复制成功。")
        except Exception as e:
            print(f"警告: 复制 使用说明与版本更新.txt 失败: {e}")
    else:
        print("警告: 未找到 使用说明与版本更新.txt，跳过复制。")


    # 2. 重命名输出文件夹
    if dist_path.exists():
        try:
            if target_path.exists():
                print(f"目标目录 {target_path} 已存在，正在删除旧目录...")
                shutil.rmtree(target_path)
            
            print(f"正在重命名输出目录: {dist_path} -> {target_path}")
            # 使用 os.rename 或 Path.rename，这里使用 Path.rename
            dist_path.rename(target_path)
            print(f"输出文件夹已成功重命名为: {target_path}")
            return True
        except Exception as e:
            print(f"错误: 重命名文件夹失败: {e}")
            return False
    else:
        print(f"错误: 找不到打包输出目录 {dist_path}")
        return False

def main():
    """
    主函数，协调整个构建流程。
    """
    print("--- 开始自动化构建流程 ---")
    
    # 1. 获取版本号
    version = get_git_version()
    if not version:
        print("构建失败: 无法获取版本号。")
        sys.exit(1)
        
    # 2. 写入版本文件
    if not write_version_file(version):
        print("构建失败: 无法写入版本文件。")
        sys.exit(1)
        
    # 2.5 生成动态文档
    tag_msg = get_git_tag_message(version)
    generate_readme(version, tag_msg)
        
    # 3. 运行 PyInstaller
    if not run_pyinstaller():
        print("构建失败: PyInstaller 执行失败。")
        sys.exit(1)

    # 4. 执行打包后操作 (复制资源和重命名)
    if not post_build_actions(version):
        print("构建失败: 打包后处理失败。")
        sys.exit(1)
        
    print("--- 构建流程成功结束 ---")

if __name__ == "__main__":
    main()
