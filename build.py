def write_version(version):
    Path("src/_version.py").write_text(
        f'# Auto-generated by build.py\n__version__ = "{version}"\n', encoding="utf-8"
    )
    print("已写入版本文件")


def run_pyinstaller():
    import subprocess

    # 清理旧的输出目录
    dist_dir = Path("dist/partyfish")
    if dist_dir.exists():
        shutil.rmtree(dist_dir)
        print("已清理旧输出目录")

    print("开始打包...")
    result = subprocess.run(
        [sys.executable, "-m", "PyInstaller", "partyfish_release.spec"]
    )
    if result.returncode != 0:
        print(f"打包失败，退出码: {result.returncode}")
        sys.exit(1)
    print("打包完成")


def post_build(version):
    import time

    dist = Path("dist/partyfish")
    target = Path(f"dist/partyfish-{version}")

    # 复制资源
    if Path("resources").exists():
        shutil.copytree("resources", dist / "resources", dirs_exist_ok=True)
        print("已复制 resources")

    # 复制说明文档
    readme = Path("使用说明与版本更新.txt")
    if readme.exists():
        shutil.copy2(readme, dist / readme.name)
        print("已复制使用说明")

    # 重命名输出目录
    if target.exists():
        for i in range(3):
            try:
                shutil.rmtree(target)
                break
            except PermissionError:
                if i < 2:
                    time.sleep(1)
                else:
                    print(f"警告: 无法删除旧目录 {target}，跳过")
                    return
    dist.rename(target)
    print(f"输出目录: {target}")


def main():
    print("=== 开始构建 ===")
    write_version(VERSION)
    generate_readme(VERSION, RELEASE_NOTES)
    run_pyinstaller()
    post_build(VERSION)
    print("=== 构建完成 ===")


if __name__ == "__main__":
    main()
