import subprocess
import sys
import shutil
import os
from pathlib import Path

def get_git_version():
    """
    通过 'git describe --tags --abbrev=0' 命令获取最新的 git tag。
    """
    try:
        # 执行 git 命令
        result = subprocess.run(
            ["git", "describe", "--tags", "--abbrev=0"],
            capture_output=True,
            text=True,
            check=True,
            encoding='utf-8'
        )
        # 获取并清理版本号字符串
        version = result.stdout.strip()
        if version:
            print(f"成功获取版本号: {version}")
            return version
        else:
            print("错误: Git tag 为空。")
            return None
    except FileNotFoundError:
        print("错误: 未找到 Git 命令。请确保 Git 已安装并已在系统 PATH 中。")
        return None
    except subprocess.CalledProcessError as e:
        # 如果 git 命令返回非零退出码，则捕获异常
        print(f"错误: 'git describe' 执行失败。您是否已经创建了至少一个 tag？")
        print(f"Git 错误信息: {e.stderr.strip()}")
        return None

def get_git_tag_message(tag_name):
    """
    获取指定 tag 的注释信息作为更新日志。
    """
    try:
        # git for-each-ref refs/tags/{tag_name} --format="%(contents)"
        # 注意：Windows下需要处理引号，subprocess会自动处理list参数
        result = subprocess.run(
            ["git", "for-each-ref", f"refs/tags/{tag_name}", "--format=%(contents)"],
            capture_output=True,
            text=True,
            check=True,
            encoding='utf-8'
        )
        msg = result.stdout.strip()
        if not msg:
            return "暂无更新日志。"
        return msg
    except Exception as e:
        print(f"获取 Tag 信息失败: {e}")
        return "获取更新日志失败。"

